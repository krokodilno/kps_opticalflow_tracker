def close_to_mask(coo, mask, n=10, share_th=0.01):
    y, x = coo
    nb = cell_neighbors(mask, x, y, n)
    nb = nb.ravel()
    mask_cnt = len(nb[nb==0])
    mask_share = mask_cnt / float(len(nb))
    
    return mask_share > share_th

